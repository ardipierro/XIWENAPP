/**
 * Emoji Picker Component
 * @param {Object} props
 * @param {Function} props.onSelect - Callback when emoji is selected
 * @param {Function} props.onClose - Callback to close picker
 */
function EmojiPicker({ onSelect, onClose }) {
  const [selectedCategory, setSelectedCategory] = useState('smileys');

  const handleEmojiClick = (emoji) => {
    onSelect(emoji);
  };

  return (
    <div className="emoji-picker">
      <div className="emoji-picker-header">
        <span className="emoji-picker-title">Emojis</span>
        <button className="emoji-picker-close" onClick={onClose}>
          <X size={18} />
        </button>
      </div>

      <div className="emoji-categories">
        {Object.entries(EMOJI_CATEGORIES).map(([key, category]) => (
          <button
            key={key}
            className={`emoji-category-btn ${selectedCategory === key ? 'active' : ''}`}
            onClick={() => setSelectedCategory(key)}
            title={category.name}
          >
            {category.icon}
          </button>
        ))}
      </div>

      <div className="emoji-grid">
        {EMOJI_CATEGORIES[selectedCategory].emojis.map((emoji, index) => (
          <button
            key={index}
            className="emoji-btn"
            onClick={() => handleEmojiClick(emoji)}
            title={emoji}
          >
            {emoji}
          </button>
        ))}
      </div>
    </div>
  );
}

export default EmojiPicker;
